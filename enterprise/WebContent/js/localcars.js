function LocalCars(para_carpos,para_marker,para_textmarker) {
 this.carpos=para_carpos;
 this.marker=para_marker;
 this.textMarker=para_textmarker;
}
LocalCars.prototype.getCarPos=function(){
	return this.carpos;
}
LocalCars.prototype.getMarker=function(){
	return this.marker;
}
LocalCars.prototype.setMarker=function(marker){
	this.marker=marker;
}
LocalCars.prototype.getTextMarker=function(){
	return this.textMarker;
}
LocalCars.prototype.setTextMarker=function(marker){
	this.textMarker=marker;
}
