
// 屏蔽鼠标右键
document.oncontextmenu = function(event) {
	self.event.returnValue = false;
	return false;
};

// 屏蔽全选
document.onselectstart = function(event) {
    self.event.returnValue = false;
    return false;
};

// 屏蔽剪切
document.oncut = function(event) {
    self.event.returnValue = false;
    return false;
};

// 屏蔽复制
document.oncopy = function(event) {
    self.event.returnValue = false;
    return false;
};