package com.etrans.bubiao.entities;

import java.io.Serializable;

/**
 * @author lihaiyan
 * @version 1.0
 * @brief
 */
@SuppressWarnings("serial")
public class SessionVehicle implements Serializable
{

	private String vehicleId;// 车辆Id
	private String registrationNo; //车牌号
	private String commNo;// 终端号
	private String gnssCenterId;//平台接入码
	private String registrationColor;//车牌颜色
	public String getVehicleId()
	{
		return vehicleId;
	}
	public void setVehicleId(String vehicleId)
	{
		this.vehicleId = vehicleId;
	}
	public String getRegistrationNo()
	{
		return registrationNo;
	}
	public void setRegistrationNo(String registrationNo)
	{
		this.registrationNo = registrationNo;
	}
	public String getCommNo()
	{
		return commNo;
	}
	public void setCommNo(String commNo)
	{
		this.commNo = commNo;
	}
	public String getGnssCenterId()
	{
		return gnssCenterId;
	}
	public void setGnssCenterId(String gnssCenterId)
	{
		this.gnssCenterId = gnssCenterId;
	}
	public String getRegistrationColor()
	{
		return registrationColor;
	}
	public void setRegistrationColor(String registrationColor)
	{
		this.registrationColor = registrationColor;
	}
	
	
	
	
	
	
}
