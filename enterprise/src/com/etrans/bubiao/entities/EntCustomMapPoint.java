package com.etrans.bubiao.entities;

import java.io.Serializable;


public class EntCustomMapPoint implements Serializable {
 
	private static final long serialVersionUID = -7831505902529169852L;
	
	private Long id;
	
	private String name;
	
	private Long mapLayerID;
	
	private Float longitude;
	
	private Float latitude;
	
	private Long mapIconID;
	
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Long getMapLayerID() {
		return mapLayerID;
	}

	public void setMapLayerID(Long mapLayerID) {
		this.mapLayerID = mapLayerID;
	}

	public Float getLongitude() {
		return longitude;
	}

	public void setLongitude(Float longitude) {
		this.longitude = longitude;
	}

	public Float getLatitude() {
		return latitude;
	}

	public void setLatitude(Float latitude) {
		this.latitude = latitude;
	}

	public Long getMapIconID() {
		return mapIconID;
	}

	public void setMapIconID(Long mapIconID) {
		this.mapIconID = mapIconID;
	}
	
	
	
}
